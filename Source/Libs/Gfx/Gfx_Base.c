void Gfx_DrawChar(U16 x, U16 y, char character, U8 color) {
 const U8 *bitmap = Font8x8[(U8)character];
  for (U16 row = 0; row < 8; row++) {
    for (U16 col = 0; col < 8; col++) {
      if (bitmap[row] & (1 <<  col)) {
        Gfx.Draw.Pixel(x + col, y + row, color);
      }
    }
  }
}



void Gfx_DrawString(U16 x, U16 y, const char *string, U8 color) {
  if (!string)
    return;
  
  for (const char *ptr = string; *ptr; ptr++) {
    Gfx.Draw.Char(x, y, *ptr, color);

    x += 8;
    if (x >= _ScreenWidth) {
      x = 0;
      y += 8;
    }
  }
}
